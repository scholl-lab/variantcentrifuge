    """
    Compare Python COAST output against R AllelicSeries golden reference values.

    R and Python use different RNGs (base::sample vs numpy.random.default_rng),
    so identical seeds produce different data. Validation strategy:

    1. **Null scenarios (1, 3, 4, 5):** Both R and Python omnibus p should be
       non-significant (p > 0.01). We verify Python matches the R behavior
       pattern (null → moderate p) rather than exact values.

    2. **Signal scenario (2):** Both R and Python should detect the signal
       (omnibus p < 0.01). We verify Python agrees on significance direction.

    3. **Component-level:** All 8 R golden values are valid p-values in (0, 1].
       Python's corresponding components should also be valid.

    Generated by: Rscript scripts/generate_coast_golden.R
    """

    def test_r_golden_scenario_1_null_is_nonsignificant(self):
        """R golden scenario 1 (binary null): omnibus p > 0.01 (no signal)."""
